		valorTotalItem = produto.ValorTotalBruto.ToString();
		valorDesconto = produto.ValorDesconto.ToString();
		movFisica = "0";
		cstIcms = produto.IcmsCst;
		cfop = produto.CfopEntrada;
		codNatureza = "";
		valorBaseIcms = produto.IcmsValorBaseCalculo.ToString();
		aliquotaIcms = produto.IcmsAliquota.ToString();
		valorIcms = produto.IcmsValor.ToString();
		valorBaseIcmsSt = produto.IcmsBaseCalculoSt.ToString();
		aliquotaIcmsSt = produto.IcmsAliquota.ToString();
		valorIcmsSt = produto.IcmsValorSt.ToString();
		indicadorPeriodoApuracaoIpi = "0";
		cstIpi = produto.IpiCst;
		codEnqIpi = "999";
		baseCalculoIpi = produto.IpiValorBaseCalculo.ToString();
		aliquotaIpi = produto.IpiAliquota.ToString();
		valorIpi = produto.IpiValor.ToString();
		cstPis = produto.PisCst;
		valorBaseCalculoPis = produto.PisValorBaseCalculo.ToString();
		aliquotaPis = produto.PisAliquota.ToString();
		quantidadeBaseCalculoPis = "";
		quantidadeALiquotaPis = "";
		valorPis = produto.PisValor.ToString();
		cstCofins = produto.CofinsCst;
		valorBaseCalculoCofins = produto.CofinsValorBaseCalculo.ToString();
		aliquotaCofins = produto.CofinsAliquota.ToString();
		quantidadeBaseCalculoCofins = "";
		quantidadeALiquotaCofins = "";
		valorCofins = produto.CofinsValor.ToString();
		contaContabil = produto.CodigoContaReduzida.ToString();
	}

	public classeC170(NotaFiscalNotasFiscaisItens produto)
	{
