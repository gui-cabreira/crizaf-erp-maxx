		valorBaseIcms = produto.ValorBaseICMS.ToString();
		aliquotaIcms = produto.AliquotaICMS.ToString();
		valorIcms = produto.ValorICMS.ToString();
		valorBaseIcmsSt = produto.ValorBaseAgragadaICMSST.ToString();
		aliquotaIcmsSt = produto.PercentualICMSSTAgregado.ToString();
		valorIcmsSt = produto.ValorICMSTotal.ToString();
		indicadorPeriodoApuracaoIpi = "0";
		cstIpi = produto.TipoIncidenciaIpi;
		codEnqIpi = produto.CodigoEnquadramentoIPI.ToString();
